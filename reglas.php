    <h2>Preparación: </h2>

<h3>Cada jugador elige un color (rojo, azul, amarillo o verde).
Se puede jugar de 2, 3 o 4 jugadores.
Cada jugador coloca sus 4 fichas en la prisión (su zona de inicio).</h3>

<h2>Juego:</h2>
<h3> 1-Para sacar una ficha de la prisión, necesitas obtener un 6 en el dado.</h3>

<h3>2-Si sacas un 6, colocás una ficha en la salida y avanzás 6 casilleros.</h3>

<h3>3-Si ya tenés fichas en juego, podés elegir cuál mover.</h3>

<h3>4-El movimiento siempre sigue el sentido de las agujas del reloj.</h3>

<h3>5-Si una ficha cae en una casilla donde hay una ficha de otro jugador:</h3>

<h3>La “come”, es decir, la ficha del rival vuelve a su prisión.</h3>

<h3>Pero si esa ficha estaba sobre una estrella, no puede ser comida durante 2 turnos.</h3>

<h3>Para entrar al casillero final (la meta), necesitás el número exacto en el dado. Por ejemplo: si te faltan 4 casilleros y sacás un 4, llegás justo. Si sacás un 6, avanzás hasta la meta y retrocedés 2 casilleros. Si sacás un 5, avanzás hasta la meta y luego retrocedés 1 casillero.</h3>

  <h3>El primer jugador que logre llevar sus 4 fichas al final gana la partida.</h3>

